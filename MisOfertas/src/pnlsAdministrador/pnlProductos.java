    /**
     * Funcion que busca un producto en la base de datos por id 
     * Luego de encontrado el producto se despliegan sus datos en los campos de texto
     */
    private void buscarProducto() {
        Producto pro = new Producto();
        pro = pro.buscar(Integer.parseInt(txtBuscar.getText()), conn);
        if(!pro.getNombre().equals("ERROR")){
            vistaDefault();
            txtBuscar.setText(String.valueOf(pro.getId()));
            txtId.setText(String.valueOf(pro.getId()));
            txtNombre.setText(pro.getNombre());
            txtDescripcion.setText(pro.getDescripcion());
            txtFechaIngreso.setDate(pro.getFecha_ing());
            
            ddlEstado.setSelectedIndex(cargarE(pro.getEstado()));
            txtSeguro.setText(String.valueOf(pro.getStk_seguro()));
            txtStockSucur.setText(String.valueOf(pro.getStk_sucur()));
            ddlRubro.setSelectedItem(pro.getRubro());
            txtValor.setText(String.valueOf(pro.getValor()));
            
            Empresa em = new Empresa();
            Sucursal suc = new Sucursal();
            suc = suc.buscar(pro.getId_sucur(), conn);
            em = em.buscar(suc.getEmpresaRut(), conn);
            ddlEmpresa2.getModel().setSelectedItem(em);
            ddlSucursal2.getModel().setSelectedItem(suc);
            
            btnNewProducto.setEnabled(true);
            btnModificar.setEnabled(true);
            btnEliminar.setEnabled(true);
        }else if(txtNombre.getText().equals("Ingrese ID del producto")){
            vistaDefault();
            lblRespuestaBuscar.setText("Ingrese ID antes de Buscar");
        }else{
            lblRespuestaBuscar.setText("No hay registros de la Sucursal con ID: " + txtBuscar.getText());
            vistaDefault();
        }
    }
    
    /**
     * Funcion que valida los campos de texto de acuerdo a las reglas de negocio
     * @return true o false dependiendo de si los campos de texto pasaron la prueba
     */
    private boolean validadorCampos() {
        boolean validar = true;
        if (txtNombre.getText().trim().isEmpty()) {
            validar = false;
        }
        if (txtDescripcion.getText().isEmpty()) {
            validar = false;
        }
        if (txtSeguro.getText().isEmpty()) {
            validar = false;
        }
        if (ddlRubro.getSelectedIndex() == 0) {
            validar = false;
        }
        if (txtValor.getText().isEmpty()) {
            validar = false;
        }if (txtFechaIngreso.getDate() == null) {
            validar = false;
        }
        if (txtStockSucur.getText().isEmpty()) {
            validar = false;
        }
        if (ddlRubro.getSelectedIndex() == 0) {
            validar = false;
        }
        if (ddlEmpresa2.getSelectedIndex() == 0) {
            validar = false;
        }

        if (ddlSucursal2.getSelectedIndex() == 0) {
            validar = false;
        }
        return validar;
    }

    /**
     * Funcion que capta lo que se seleccionar en el ddlestado
     * @return Recepciona un char representativo del estado en que se encuentra el producto 
     */
    private char cargarEstado() {
        char var = 0;
            if (ddlEstado.getSelectedIndex() == 0) {
                var = 'd';
            }else if (ddlEstado.getSelectedIndex() == 1) {
                var = 'f';
            }else if (ddlEstado.getSelectedIndex() == 2) {
                var = 'v';
            }
        return var;
    }

    /**
     * Funcion que carga el ddl estado de producto 
     * @param e estado del producto en la base de datos 
     * @return el numero del index el cual se cargara en el ddl 
     */
    private int cargarE(char e) {
        int index = 0;
            if (e == 'd') {
                index = 0;
            }else if (e == 'f'){
                index = 1;
            }else if (e == 'v'){
                index = 2;
            }
        return index;
    }
    
