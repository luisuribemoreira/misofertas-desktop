    /**
     * Funcion que agrega o modifica un producto 
     * @param evt Evento de presionar el boton agregar
     */
    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        try{
            if (!validadorCampos()){
                throw new Exception ("Ingrese todos los datos");
            }
            Producto pro = new Producto();
            pro.setNombre(txtNombre.getText());
            pro.setDescripcion(txtDescripcion.getText());
            java.util.Date utilDate = (java.util.Date) txtFechaIngreso.getDate();
            java.sql.Date sqlDate = new java.sql.Date(utilDate.getTime());
            pro.setFecha_ing(sqlDate);
            char estado = cargarEstado();
            pro.setEstado(estado);
            if (!validarStock(Integer.parseInt(txtSeguro.getText()))) {
                throw new Exception ("El stock seguro no puede ser negativo");
            }
            pro.setStk_seguro(Integer.parseInt(txtSeguro.getText()));
            pro.setStk_sucur(Integer.parseInt(txtStockSucur.getText()));
            pro.setRubro(obtenerRubro());
            pro.setDesc_rubro(obtenerDescRubro());
            if (!validarValor(Integer.parseInt(txtValor.getText()))) {
                throw new Exception ("El stock seguro no puede ser negativo");
            }
            pro.setValor(Integer.parseInt(txtValor.getText()));
            //Sera cambiado 
            try{
                Sucursal suc = (Sucursal)ddlSucursal2.getModel().getSelectedItem();
                pro.setId_sucur(suc.getId_sucur());
            }catch(Exception e){
                
            }
            int respuesta = 0;
        
            if (txtId.getText().equals("")) {
                int a = JOptionPane.showConfirmDialog(null, "Â¿Desea Registrar este producto?", "Message",  JOptionPane.YES_NO_OPTION);
                if (a == 0 ) {
                    respuesta = pro.agregar(conn);
                    if(respuesta == 1){
                        JOptionPane.showMessageDialog(null,"El producto fue registrado",null, JOptionPane.INFORMATION_MESSAGE, null);
                        try {
                            cargarTabla();
                            vistaDefault();
                        } catch (SQLException ex) {
                            Logger.getLogger(pnlProductos.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    }else{
                        JOptionPane.showMessageDialog(null,"El Producto ya fue registrado",null, JOptionPane.INFORMATION_MESSAGE, null);
                    }
                }
            }else{
                int a = JOptionPane.showConfirmDialog(null, "Â¿Desea Modificar este Producto?", "Message",  JOptionPane.YES_NO_OPTION);
                if(a == 0){
                    pro.setId(Integer.parseInt(txtId.getText()));
                    respuesta = pro.modificar(conn);
                    if(respuesta == 1){
                        JOptionPane.showMessageDialog(null,"El Producto fue Modificado",null, JOptionPane.INFORMATION_MESSAGE, null);
                        try {
                            cargarTabla();
                            vistaDefault();
                        } catch (SQLException ex) {
                            Logger.getLogger(pnlProductos.class.getName()).log(Level.SEVERE, null, ex);
                        }
                    }else{
                        JOptionPane.showMessageDialog(null,"Ocurrio un error al Modificar",null, JOptionPane.INFORMATION_MESSAGE, null);
                    }
                }
            }
        }catch(Exception ex){
            JOptionPane.showMessageDialog(null,"" + ex.getMessage(),null, JOptionPane.INFORMATION_MESSAGE, null);
        } 
        
    }//GEN-LAST:event_btnAgregarActionPerformed

