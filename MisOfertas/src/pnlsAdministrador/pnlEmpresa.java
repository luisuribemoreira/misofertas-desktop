    /**
     * Método que busca la empresa y transfiere la información a los campos asociados.
     */
    private void buscarEmpresa() {
        Empresa emp = new Empresa();
        emp = emp.buscar(txtBuscar.getText(), conn);
        if(!emp.getNombre().equals("ERROR")){
            vistaDefault();
            txtBuscar.setText(emp.getRut());
            txtRut.setText(emp.getRut());
            txtNombre.setText(emp.getNombre());
            txtDireccion.setText(emp.getDireccion());
            txtRazonSocial.setText(emp.getRazon_social());
            btnNewEmpresa.setEnabled(true);
            btnModificar.setEnabled(true);
            btnEliminar.setEnabled(true);
        }else if(txtRut.getText().equals("Ingrese Rut de la Empresa")){
            vistaDefault();
            lblRespuestaBuscar.setText("Ingrese Rut antes de Buscar");
        }else{
            lblRespuestaBuscar.setText("No hay registros de la Empresa con RUT: " + txtBuscar.getText());
            vistaDefault();
        }
    }

    /**
     * Método que valida el rut entregado
     * @param rut Rut entregado como parametro
     * @return Boolean correspondiente si es o no valido el rut
     */
    private boolean validarRut(String rut) {
        boolean isRut = rut.matches("^[0-9]+[-|‐]{1}[0-9kK]{1}$");
        if (!isRut) {
            lblRutRespuesta.setText("El Formato del Rut es Incorrecto");
            return false;
        }
        lblRutRespuesta.setText("");
        boolean validacion = false;
        try {
            rut =  rut.toUpperCase();
            rut = rut.replace(".", "");
            rut = rut.replace("-", "");
            int rutAux = Integer.parseInt(rut.substring(0, rut.length() - 1));

            char dv = rut.charAt(rut.length() - 1);

            int m = 0, s = 1;
            for (; rutAux != 0; rutAux /= 10) {
                s = (s + rutAux % 10 * (9 - m++ % 6)) % 11;
            }
            if (dv == (char) (s != 0 ? s + 47 : 75)) {
                validacion = true;
            }

        } catch (java.lang.NumberFormatException e) {
        } catch (Exception e) {
        }
        return validacion;
    }

    /**
     * Método que valida que los campos no estén vacios
     * @return Boolean que corresponde si hay o no campos vacios
     */
    private boolean validadorCampos() {
        boolean validador = true;
        if (txtNombre.getText().trim().isEmpty()) {
            validador = false;
        }
        if (txtDireccion.getText().trim().isEmpty()) {
            validador = false;
        }
        if (txtRazonSocial.getText().trim().isEmpty()) {
            validador = false;
        }
        
        return validador;
    }


