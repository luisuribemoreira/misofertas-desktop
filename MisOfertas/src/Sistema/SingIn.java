    /**
     * Funcion que se activa al momento de presionar el boton para ingresar al sistema
     * @param evt Evento de presionar el boton 
     */
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        String mensajeError = "";
        
        mensajeError = verificCamposVacios();
        
        user_conectado = user_conectado.logear(txtUser.getText(), txtPass.getText(), conn);

        if(!user_conectado.getPerfil().equals("ERROR")){
            emp_conectado.buscarEmpleadoPorUserName(user_conectado.getUsername(), conn);
            if (user_conectado.getPerfil().equals("CONSUMIDOR")) {
                lblMsgError.setText("Usuario no tiene permisos");
            }else if(user_conectado.getPerfil().equals("GERENTE")) {
                SistemaGerente sistema = new SistemaGerente();
                this.setVisible(false);
                sistema.setVisible(true);
            }else if(user_conectado.getPerfil().equals("ADMINISTRADOR")) 
            {    
                SistemaAdmin sistema = new SistemaAdmin();
                this.setVisible(false);
                sistema.setVisible(true);
            }
            else if(user_conectado.getPerfil().equals("ENCARGADO")) 
            {
                SistemaEncargado sistema = new SistemaEncargado();
                this.setVisible(false);
                sistema.setVisible(true);
            }
        }else{
            if(mensajeError.equals("")){
                lblMsgError.setText("Usuario No Registrado");
            }else{
                lblMsgError.setText(mensajeError);
            }  
        }
    }//GEN-LAST:event_jButton1ActionPerformed

