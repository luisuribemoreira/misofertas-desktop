    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        Empresa emp = new Empresa();
        emp.setRut(txtRut.getText());
        int respuesta = 0;
        int a = JOptionPane.showConfirmDialog(null, "¿Desea Eliminar esta Empresa?", "Message",  JOptionPane.YES_NO_OPTION);
            if(a == 0){
                respuesta = emp.eliminar(conn);
                if(respuesta == 1){
                    JOptionPane.showMessageDialog(null,"La empresa fue Eliminada",null, JOptionPane.INFORMATION_MESSAGE, null);
                    try {
                        cargarTabla();
                        vistaDefault();
                    } catch (SQLException ex) {
                        Logger.getLogger(Empresa_List.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }else{
                    JOptionPane.showMessageDialog(null,"Error al Eliminar",null, JOptionPane.INFORMATION_MESSAGE, null);
                }
            }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        Empresa emp = new Empresa();
        emp.setRut(txtRut.getText());
        emp.setNombre(txtNombre.getText());
        emp.setDireccion(txtDireccion.getText());
        emp.setRazon_social(txtRazonSocial.getText());
        int respuesta = 0;
        
        if(txtRut.isEnabled()){
            int a = JOptionPane.showConfirmDialog(null, "¿Desea Registrar esta Empresa?", "Message",  JOptionPane.YES_NO_OPTION);
            if(a == 0){
                respuesta = emp.agregar(conn);
                if(respuesta == 1){
                    JOptionPane.showMessageDialog(null,"La empresa fue Registrada",null, JOptionPane.INFORMATION_MESSAGE, null);
                    try {
                        cargarTabla();
                        vistaDefault();
                    } catch (SQLException ex) {
                        Logger.getLogger(Empresa_List.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }else{
                    JOptionPane.showMessageDialog(null,"La empresa ya fue registrada",null, JOptionPane.INFORMATION_MESSAGE, null);
                }
            } 
        }else{
            int a = JOptionPane.showConfirmDialog(null, "¿Desea Modificar esta Empresa?", "Message",  JOptionPane.YES_NO_OPTION);
            if(a == 0){
                respuesta = emp.modificar(conn);
                if(respuesta == 1){
                    JOptionPane.showMessageDialog(null,"La empresa fue Modificada",null, JOptionPane.INFORMATION_MESSAGE, null);
                    try {
                        cargarTabla();
                        vistaDefault();
                    } catch (SQLException ex) {
                        Logger.getLogger(Empresa_List.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }else{
                    JOptionPane.showMessageDialog(null,"Ocurrio un error al Modificar",null, JOptionPane.INFORMATION_MESSAGE, null);
                }
            }
        }
    }//GEN-LAST:event_btnAgregarActionPerformed

